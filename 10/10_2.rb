closing_char_matcher = /(\)|\]|\}|\>)/
pair_matcher = /(\(\)|\[\]|\{\}|\<\>)/

CLOSING_CHAR_SCORES = {
  ")" => 1,
  "]" => 2,
  "}" => 3,
  ">" => 4,
}

PAIR_DICTIONNARY = {
  "(" => ")",
  "[" => "]",
  "{" => "}",
  "<" => ">",
}

pair_stripper = lambda do |line, previous_version = nil|
  processed_line = line.gsub(pair_matcher, "")

  if processed_line == line
    line
  else
    pair_stripper.call(processed_line, line)
  end
end

is_incomplete = lambda do |line|
  !line.match?(closing_char_matcher) && line.length > 0
end

incomplete_line_scorer = lambda do |line|
  line
    .reverse
    .split("")
    .inject(0) do |total_score, char|
      char_score = CLOSING_CHAR_SCORES[PAIR_DICTIONNARY[char]]

      (total_score * 5) + char_score
    end
end

incomplete_line_scores =
  advent
    .filter_map do |line|
      line_missing_pairs = pair_stripper.call(line)

      if is_incomplete.call(line_missing_pairs)
        incomplete_line_scorer.call(line_missing_pairs)
      end
    end

incomplete_line_scores
  .sort
  .then do |sorted_scores|
    middle_index = (sorted_scores.length / BigDecimal(2)).floor

    sorted_scores[middle_index]
  end
